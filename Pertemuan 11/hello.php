<?php
    // echo adalah cara paling umum menampilkan output
    echo "Hello, World!";
    // Atau dengan print — hampir sama, tapi hanya bisa 1 argumen
    print "<br>Hello dengan print!";
    // var_dump — sangat berguna untuk debugging (tampilkan nilai + tipe)
    var_dump("Hello"); // string(5) "Hello"
    var_dump(42); // int(42)
    var_dump(true); // bool(true)
?>
